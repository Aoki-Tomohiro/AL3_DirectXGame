#include "MathFunction.h"

void Move(Vector3& transform, Vector3& move) {
	transform.x += move.x;
	transform.y += move.y;
	transform.z += move.z;
}

Vector3 Subtract(const Vector3& v1, const Vector3& v2) {
	Vector3 result;
	result.x = v1.x - v2.x;
	result.y = v1.y - v2.y;
	result.z = v1.z - v2.z;
	return result;
}

